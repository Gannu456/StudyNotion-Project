export function logout(navigate) {
  return (dispatch) => {
    dispatch(setToken(null))
    dispatch(setUser(null))
    dispatch(resetCart())
    localStorage.removeItem("token")
    localStorage.removeItem("user")
    toast.success("Logged Out")
    navigate("/")
  }
}

export function loginWithPasskey(email, credential = null) {
  return async (dispatch) => {
    const toastId = toast.loading("Loading...");
    dispatch(setLoading(true));
    try {
      if (!credential) {
        // Step 1: Fetch authentication options
        const response = await apiConnector("POST", LOGIN_WITH_PASSKEY_API, {
          email,
        });

        console.log("Login with Passkey Options Response:", response);

        if (!response.data.success) {
          throw new Error(response.data.message);
        }

        return response.data; // Return options for authentication
      } else {
        // Step 2: Verify the authentication response
        const verificationResponse = await apiConnector(
          "POST",
          VERIFY_PASSKEY_LOGIN_API,
          { email, credential }
        );

        console.log("Verify Passkey Login Response:", verificationResponse);

        if (!verificationResponse.data.success) {
          throw new Error(verificationResponse.data.message);
        }

        // Save the token and user data
        dispatch(setToken(verificationResponse.data.token));
        dispatch(setUser(verificationResponse.data.user));
        localStorage.setItem(
          "token",
          JSON.stringify(verificationResponse.data.token)
        );
        toast.success("Login Successful");
        return verificationResponse.data;
      }
    } catch (error) {
      console.error("Error during passkey login:", error);
      toast.error(error.message || "Failed to login with passkey.");
      return {success: false, message: error.message || "Failed to login with passkey."};
    } finally {
      dispatch(setLoading(false));
      toast.dismiss(toastId);
    }
  };
}